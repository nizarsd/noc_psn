function [Qi] = wire_load(h,cl,tau,rs,ls,r,l,c,tr)





n3 = 1/(factorial(7)*tr)*r^3*c^4*h^7 +  1/(factorial(6)*tr)*cl*r^3*c^3*h^6 + 2/(factorial(5)*tr)*r*l*c^3*h^5 + 2/(factorial(4)*tr)*cl*r*l*c^2*h^4 ;
n2 = 1/(factorial(5)*tr)*r^2*c^3*h^5 +  1/(factorial(4)*tr)*cl*r^2*c^2*h^4 + 1/(factorial(3)*tr)*l*c^2*h^3+ 1/(factorial(2)*tr)*cl*l*c*h^2;
n1 = 1/(factorial(3)*tr)*r*c^2*h^3 +1/(factorial(2)*tr)*cl*r*c*h^2;
n0 = 1/(tr)*(c*h + cl);
d4 = 1/(factorial(8)*tr)*r^4*c^4*h^8 +...
        1/tr*(1/(factorial(7))*rs*r^3*c^4 + 1/(factorial(7))*cl*r^4*c^3)*h^7 +...
       (1/factorial(6)*r^3*c^3 + 1/tr*(3/factorial(6)*r^2*l*c^3 + 1/factorial(6)*cl*rs*r^3*c^3))*h^6 + ...
       (1/factorial(5)*rs*r^2*c^3 + 1/factorial(5)*cl*r^3*c^2 + 1/tr*(3/factorial(5)*cl*r^2*l*c^2 + 2/factorial(5)*rs*r*l*c^3 + 1/factorial(5)*ls*r^2*c^3))*h^5 + ...
       (1/factorial(4)*cl*rs*r^2*c^2 + 2/factorial(4)*r*l*c^2 + 1/tr*(1/factorial(4)*l^2*c^2 +1/factorial(4)*cl*ls*r^2*c^2 + 2/factorial(4)*cl*rs*r*l*c^2))*h^4 + ...
       (1/factorial(3)*rs*l*c^2 + 2/factorial(3)*cl*r*l*c + 1/factorial(3)*ls*r*c^2 + 1/tr*(1/factorial(3)*cl*l^2*c + 1/factorial(3)*ls*l*c^2))*h^3 + ...
       (1/(factorial(2)*tr)*cl*ls*l*c + 1/factorial(2)*cl*rs*l*c + 1/factorial(2)*cl*ls*r*c)*h^2;
   
    
d3 = 1/(factorial(6)*tr)*r^3*c^3*h^6 + 1/tr*(1/factorial(5)*rs*r^2*c^3 + 1/factorial(5)*cl*r^3*c^2)*h^5 + ...
     (1/factorial(4)*r^2*c^2 + 1/tr*(2/factorial(4)*r*l*c^2 + 1/factorial(4)*cl*rs*r^2*c^2))*h^4 + ...
     (1/factorial(3)*cl*r^2*c + 1/factorial(3)*rs*r*c^2 + 1/tr*(1/factorial(3)*rs*l*c^2 + 1/factorial(3)*ls*r*c^2 + 2/factorial(3)*cl*r*l*c))*h^3 + ...
     (1/factorial(2)*cl*rs*r*c + 1/factorial(2)*l*c + 1/tr*(1/factorial(2)*cl*rs*l*c + 1/factorial(2)*cl*ls*r*c))*h^2 + ...
     (ls*c + cl*l)*h + cl*ls;
 
d2 = 1/(factorial(4)*tr)*r^2*c^2*h^4 + ...
     1/tr*(1/factorial(3)*rs*r*c^2 + 1/factorial(3)*cl*r^2*c)*h^3 + ...
     (1/factorial(2)*r*c + 1/tr*(1/factorial(2)*cl*rs*r*c + 1/factorial(2)*l*c))*h^2 + ...
     (1/tr*(ls*c + cl*l) + rs*c + cl*r)*h + ...
     1/tr*cl*ls + cl*rs;
 
     
d1 = 1/(tr*factorial(2))*r*c*h^2 + 1/tr*(cl*r+rs*c) + 1/tr*cl*rs + 1;

d0 = 1/tr;



nn= [n3 n2 n1 n0];
dd = [d4 d3 d2 d1 d0];
[ap sp k] = residue(nn,dd);
% ap(4)=-1*sum(ap(1:3));
Qi = sum(-ap./sp);


